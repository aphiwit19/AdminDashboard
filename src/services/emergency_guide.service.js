      console.log('üìä ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Ñ‡∏π‡πà‡∏°‡∏∑‡∏≠‡∏â‡∏∏‡∏Å‡πÄ‡∏â‡∏¥‡∏ô‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à:', emergencyGuides.length, '‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£');
